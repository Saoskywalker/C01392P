#include "lib_cap0.h"
/*********************************************************
函数名:	CAP0_RD
描  述:	读取捕捉值
输入值: 无
输出值: 16位值
返回值: 无 
**********************************************************/
uint CAP0_RD(void)
{
    uint tmp_data;
    tmp_data = CAP0_DH;
    tmp_data = tmp_data<<8;
    tmp_data = tmp_data + CAP0_DL;
    return  tmp_data;
}
